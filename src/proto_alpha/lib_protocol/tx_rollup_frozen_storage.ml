let frozen_tez : Raw_context.t -> Contract_repr.t -> Tez_repr.t tzresult Lwt.t =
 fun ctxt contract ->
  Storage.Tx_rollup.Frozen_commitments.find ctxt contract >>=? function
  | None -> return Tez_repr.zero
  | Some tez -> return tez
